package com.javaprograms.javaoperations;

public class Operations {
	
	/*
	 * Addition
	 * Substraction
	 * Multiplication
	 * Division
	 * Module - remainder
	 * 
	 */

	public static void main(String[] args) {
		
		int myInt1 = 4;
		int myInt2 = 2;
		int myResult = 0;
		
		myResult = myInt1 + myInt2;
		System.out.println("Result is: " + myResult);
		
		myResult = myInt1 - myInt2;
		System.out.println("Result is: " + myResult);
		
		myResult = myInt1 * myInt2;
		System.out.println("Result is: " + myResult);
		
		myResult = myInt1 / myInt2;
		System.out.println("Result is: " + myResult);
		
		myResult = myInt1 % myInt2;
		System.out.println("Result is: " + myResult);

	}

}
